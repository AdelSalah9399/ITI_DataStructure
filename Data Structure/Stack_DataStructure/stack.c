/*---------------------------------------------------------------------------------------------------------------*/
u8 GetSize(Stack* ps)
{
	return ps->top;
}
/*---------------------------------------------------------------------------------------------------------------*/
void ClearStack(Stack* ps)
{
	ps->top=0;
}
/*---------------------------------------------------------------------------------------------------------------*/
void Display(Stack* ps)
{
	u8 top;
	u8 index=ps->top;
	for(top=ps->top;top>0;top--)
	{
		printf("element with index [%d] is %d\n",--index,ps->entry[top-1]);
	}
}