#include <stdio.h>
#include "STD_TYPES.h"
#include "stack.h"


/*---------------------------------------------------------------------------------------------------------------*/
void Initialize_Stack(Stack* ps)
{
	ps->top=0;
}
/*---------------------------------------------------------------------------------------------------------------*/
void PUSH(StackEntry e,Stack *ps)
{
	ps->entry[ps->top]=e;
	ps->top++;
}
/*---------------------------------------------------------------------------------------------------------------*/
u8 StackFull(Stack* ps)
{
	if(ps->top==MaxStack)
	{
		return 1;
	}
	else
	{
		return 0;
	}
}
/*---------------------------------------------------------------------------------------------------------------*/
void POP(StackEntry* pe,Stack* ps)
{
	ps->top--;
	*pe = ps->entry[ps->top];
}
/*---------------------------------------------------------------------------------------------------------------*/
u8 EMPTY(Stack* ps)
{
	if(ps->top==0)
	{
		return 1;
	}
	else
	{
		return 0;
	}
}
/*---------------------------------------------------------------------------------------------------------------*/
void GetTop(StackEntry* pe,Stack* ps)
{
	*pe=ps->entry[ps->top-1];
}
